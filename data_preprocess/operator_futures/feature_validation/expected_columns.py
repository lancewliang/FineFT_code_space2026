def _windowed_name(template: str, window: int) -> str:
    if "{window}" in template:
        return template.format(window=window)
    return f"{template}_{window}"


def _build_time_feature_columns() -> list[str]:
    columns = []
    for window in TIME_WINDOWS:
        for feature in PRICE_FEATURE_COLUMNS:
            columns.append(f"{feature}_log_return_{window}")
            columns.append(f"{feature}_trend_{window}")

    for suffix in OHLCV_GROUP_SUFFIXES:
        columns.append(f"log_volume{suffix}")
        for window in TIME_WINDOWS:
            for template in OHLCV_FEATURE_TEMPLATES:
                columns.append(f"{_windowed_name(template, window)}{suffix}")

    for suffix in OHLC_GROUP_SUFFIXES:
        for window in TIME_WINDOWS:
            for template in OHLC_FEATURE_TEMPLATES:
                columns.append(f"{_windowed_name(template, window)}{suffix}")
    return columns


